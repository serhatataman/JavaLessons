package com.serhat.examples.challengeOOP;

public class Additions {
    private int eachAdditionCost;
    private int numberOfLettuce;
    private int numberOfTomato;
    private int numberOfCarrot;

    public int getEachAdditionCost() {
        return eachAdditionCost;
    }

    public int getNumberOfLettuce() {
        return numberOfLettuce;
    }

    public int getNumberOfTomato() {
        return numberOfTomato;
    }

    public int getNumberOfCarrot() {
        return numberOfCarrot;
    }

    public void setEachAdditionCost(int eachAdditionCost) {
        this.eachAdditionCost = eachAdditionCost;
    }

    public void setNumberOfLettuce(int numberOfLettuce) {
        this.numberOfLettuce = numberOfLettuce;
    }

    public void setNumberOfTomato(int numberOfTomato) {
        this.numberOfTomato = numberOfTomato;
    }

    public void setNumberOfCarrot(int numberOfCarrot) {
        this.numberOfCarrot = numberOfCarrot;
    }
}
